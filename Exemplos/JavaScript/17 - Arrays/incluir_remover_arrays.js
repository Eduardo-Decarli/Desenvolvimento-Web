var lista_frutas = Array()

lista_frutas[0] = 'banana'
lista_frutas[1] = 'maça'

//Incluir elemento no final do array
//O método push() inclui um novo elemento no final do array
lista_frutas.push('uva')

//Incluir elemento no final do array
//O método unshift inclui um novo item no inicio do Array
lista_frutas.unshift('laranja')

//Excluir o elemento no final do array
lista_frutas.pop() // o método pop() deleta o ultimo elemento do array

//Excluir o elemento do inicio do array
lista_frutas.shift() // o método shift() exclui o elemento do inicio do array